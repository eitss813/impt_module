    public function _addHomePage()
    {
        $db = $this -> getDb();

        // Check if it's already been placed
        $select = new Zend_Db_Select($db);
        $select -> from('engine4_core_pages') -> where('name = ?', 'yndynamicform_index_index') -> limit(1);
        $info = $select -> query() -> fetch();

        if (empty($info)) {
            $db -> insert('engine4_core_pages', array('name' => 'yndynamicform_index_index',
                'displayname' => 'YNC - Dynamic Form - Home Page',
                'title' => 'Home Page',
                'description' => 'This is the Dynamic form home page.',
                'custom' => 0,
            ));
            $page_id = $db -> lastInsertId('engine4_core_pages');

            // top
            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'container',
                'name' => 'top',
                'parent_content_id' => null,
