        $db = $this -> getDb();

        // Check if it's already been placed
        $select = new Zend_Db_Select($db);
        $select -> from('engine4_core_pages') -> where('name = ?', 'yndynamicform_form_confirmation') -> limit(1);
        $info = $select -> query() -> fetch();

        if (empty($info)) {
            $db -> insert('engine4_core_pages', array('name' => 'yndynamicform_form_confirmation',
                'displayname' => 'YNC - Dynamic Form - Form Confirmation Page',
                'title' => 'Form Confirmation',
                'description' => 'This is the form confirmation page.',
                'custom' => 0,
            ));
            $page_id = $db -> lastInsertId('engine4_core_pages');

            // top
            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'container',
                'name' => 'top',
                'parent_content_id' => null,
                'order' => 1,
            ));
            $top_id = $db -> lastInsertId('engine4_core_content');

            // top contents
            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'container',
                'name' => 'middle',
                'parent_content_id' => $top_id,
                'order' => 1,
            ));
            $top_middle_id = $db -> lastInsertId('engine4_core_content');

            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'widget',
                'name' => 'yndynamicform.browse-menu',
                'parent_content_id' => $top_middle_id,
                'order' => 1,
            ));

            // main
            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'container',
                'name' => 'main',
                'parent_content_id' => null,
                'order' => 2,
            ));
            $main_id = $db -> lastInsertId('engine4_core_content');

            // main middle
            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'container',
                'name' => 'middle',
                'parent_content_id' => $main_id,
                'order' => 2,
                'params' => '["[]"]',
            ));
            $main_middle_id = $db -> lastInsertId('engine4_core_content');

            // Insert content
            $db -> insert('engine4_core_content', array(
                'page_id' => $page_id,
                'type' => 'widget',
                'name' => 'yndynamicform.form-details',
                'parent_content_id' => $main_middle_id,
                'order' => 1,
            ));

            $db->insert('engine4_core_content', array(
                'type' => 'widget',
                'name' => 'core.content',
                'page_id' => $page_id,
                'parent_content_id' => $main_middle_id,
                'order' => 2,
            ));

            // main right
            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'container',
                'name' => 'right',
                'parent_content_id' => $main_id,
                'order' => 2,
                'params' => '["[]"]',
            ));
            $main_right_id = $db -> lastInsertId('engine4_core_content');

            $db -> insert('engine4_core_content', array('page_id' => $page_id,
                'type' => 'widget',
                'name' => 'yndynamicform.list-related-forms',
                'parent_content_id' => $main_right_id,
                'order' => 1,
                'params' => '{"title":"Related Forms", "max":4}',
            ));
        }
    }
